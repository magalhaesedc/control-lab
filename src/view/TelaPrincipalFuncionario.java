    private void preencherTabelaReserva(List<Reserva> Lista) {
        DefaultTableModel modelo = (DefaultTableModel) cp_tabelaReservas.getModel();

        Object[] linha = new Object[5];

        modelo.setRowCount(0);
        for (Reserva reserva : Lista) {
            linha[0] = reserva.getSiape().getNome();
            linha[1] = converteData.format(reserva.getDataReserva());
            linha[2] = reserva.getLaboratorio();
            linha[3] = "De " + converteHora.format(reserva.getHorarioEntradaReserva()) + " até " + converteHora.format(reserva.getHorarioSaidaReserva());
            linha[4] = reserva.getProposito();

            modelo.addRow(linha);
        }
        cp_tabelaReservas.setModel(modelo);
    }
    
    private void preencherTabelaFeedback(List<Feedback> Lista) {
        DefaultTableModel modelo = (DefaultTableModel) cp_tabelaFeedback.getModel();

        Object[] linha = new Object[5];

        modelo.setRowCount(0);
        for (Feedback feedback : Lista) {
            linha[0] = feedback.getSiape().getNome();
            linha[1] = feedback.getLaboratorio();
            linha[2] = converteData.format(feedback.getData());
            linha[3] = feedback.getDescricao();
            linha[4] = feedback.getStatus();
            modelo.addRow(linha);
        }
        cp_tabelaFeedback.setModel(modelo);
    }
    
    
