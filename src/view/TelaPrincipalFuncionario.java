    private void cp_tabelaReservasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cp_tabelaReservasMouseClicked
        // TODO add your handling code here:

        DefaultTableModel modelo = (DefaultTableModel) cp_tabelaReservas.getModel();

        if (evt.getButton() == MouseEvent.BUTTON3 && cp_tabelaReservas.getSelectedRowCount() == 1) {
            try {
                int x = evt.getX();
                int y = evt.getY();

                int linha = cp_tabelaReservas.getSelectedRow();
                Date data = converteData.parse(modelo.getValueAt(linha, 1).toString());
                String horarioAux = modelo.getValueAt(linha, 3).toString();
                String nome = modelo.getValueAt(linha, 0).toString();

                Date horaEntrada = converteHora.parse(horarioAux.substring(3, 8));
                Date horaSaida = converteHora.parse(horarioAux.substring(13));

                if (nome.equals(txt_nomeFuncionarioCadReserva.getText())) {
                    menuReserva.show(cp_tabelaReservas, x, y);
                    reservaRecuperada = controleReserva.recuperarReserva(data, horaEntrada, horaSaida);
                } else {
                    JOptionPane.showMessageDialog(null, "Você não tem autorização de remover ou alterar essa reserva", "Control Lab Avisa", INFORMATION_MESSAGE);
                }

            } catch (Exception erro) {
                JOptionPane.showMessageDialog(null, "Erro : " + erro, "Control Lab Avisa", ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_cp_tabelaReservasMouseClicked

