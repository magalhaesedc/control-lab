    private void bt_cadastrarReservaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_cadastrarReservaActionPerformed
        // TODO add your handling code here:
        int selectedOption = JOptionPane.showConfirmDialog(null, "Deseja realmente cadastrar essa reserva?", "Control Lab Avisa", YES_NO_OPTION, QUESTION_MESSAGE);
        if (selectedOption == JOptionPane.YES_OPTION) {

            if (cp_dataReserva.getDate() == null) {
                JOptionPane.showMessageDialog(null, "Preencha todos os campos!!");
            }

            String nome = cp_listarFuncionarios.getSelectedItem().toString();

            String siape = bancoFuncionario.recuperarSiape(nome);
            String msg = "";
            String proposito = cp_proposito.getText();
            String laboratorio = cp_laboratorioReserva.getSelectedItem().toString();

            Date auxDataReserva = cp_dataReserva.getDate();
            String aux2dataReserva = converteData.format(auxDataReserva);

            String horaEntrada = cp_horaEntrada.getSelectedItem().toString();
            String minutoEntrada = cp_minutoEntrada.getSelectedItem().toString();
            String horaSaida = cp_horaSaida.getSelectedItem().toString();
            String minutoSaida = cp_minutoSaida.getSelectedItem().toString();

            try {
                Date dataReserva = converteData.parse(aux2dataReserva);
                Date horarioEntradaReserva = converteHora.parse(horaEntrada + ":" + minutoEntrada);
                Date horarioSaidaReserva = converteHora.parse(horaSaida + ":" + minutoSaida);

                msg = controleReserva.cadastrar(nome, siape, dataReserva, horarioEntradaReserva, horarioSaidaReserva, proposito, laboratorio);
                JOptionPane.showMessageDialog(this, msg);
            } catch (ParseException ex) {
                JOptionPane.showMessageDialog(this, "Data ou Hora inválidas!");
            }

            if (msg.equals("Reserva cadastrada com sucesso!")) {
                cp_dataReserva.setDate(null);
                cp_horaEntrada.setSelectedItem("--");
                cp_minutoEntrada.setSelectedItem("--");
                cp_horaSaida.setSelectedItem("--");
                cp_minutoSaida.setSelectedItem("--");
                cp_proposito.setText("");
                cp_laboratorioReserva.setSelectedItem("-------");
                List<Reserva> Lista = controleReserva.pesquisar("");
                preencherTabelaReserva(Lista);
            }
        }
    }//GEN-LAST:event_bt_cadastrarReservaActionPerformed

    private void alterarStatusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_alterarStatusActionPerformed

        int selectedOption = JOptionPane.showConfirmDialog(null, "Deseja realmente alterar o status do Feedback selecionado?", "Control Lab Avisa", YES_NO_OPTION, QUESTION_MESSAGE);
        if (selectedOption == JOptionPane.YES_OPTION) {
            String[] status = {"Pendente", "Em andamento", "Finalizado", "Cancelado", "Inconsistente"};
            int indiceStatusAtual = 0;

            for (int i = 0; i < status.length; i++) {
                if (status[i].equals(feedbackRecuperado.getStatus())) {
                    indiceStatusAtual = i;
                    break;
                }
            }

            String novoStatus = (String) JOptionPane.showInputDialog(null,
                    "Feedback criado por: " + feedbackRecuperado.getSiape().getNome()
                    + "\n\nSeleciona um novo Status para o Feedback selecionado.", // Título
                    "Alterar Status do Feedback", // Subtítulo
                    JOptionPane.QUESTION_MESSAGE, null,
                    status, // Array com as opções
                    status[indiceStatusAtual]); // Padrão

            if (novoStatus != null && !novoStatus.isEmpty()) {

                Feedback feedback = feedbackRecuperado;
                feedback.setStatus(novoStatus);
                String msg = controleFeedback.alterar(feedback);

                JOptionPane.showMessageDialog(null, msg);

                if (msg.equals("Feedback alterado com sucesso!")) {
                    preencherTabelaFeedback(controleFeedback.atualizar(""));
                }
            }
        }
    }//GEN-LAST:event_alterarStatusActionPerformed

