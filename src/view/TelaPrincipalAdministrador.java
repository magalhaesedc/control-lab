    private void bt_gerarRelatorioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_gerarRelatorioActionPerformed
        // TODO add your handling code here:

        if (cp_dataInicial.getDate() == null || cp_dataFinal.getDate() == null) {

            JOptionPane.showMessageDialog(null, "Verifique se as datas estão corretas!", "Control Lab Avisa", INFORMATION_MESSAGE);

        } else {

            Date auxdataInicial = cp_dataInicial.getDate();
            Date auxdataFinal = cp_dataFinal.getDate();

            if (auxdataInicial.after(auxdataFinal)) {
                
                JOptionPane.showMessageDialog(null, "A Data Inicial não pode ser maior que a Data Final", "Control Lab Avisa", QUESTION_MESSAGE);
            
            } else {
                boolean tipoRelatorio = bt_reservas.isSelected();

                String dataInicial = converteData.format(auxdataInicial);
                String dataFinal = converteData.format(auxdataFinal);

                System.out.println("Data inicial: " + auxdataInicial);
                System.out.println("Data final: " + auxdataFinal);
                System.out.println("Data inicial Conv: " + dataInicial);
                System.out.println("Data final Conv: " + dataFinal);

                try {
                controleRelatorio.gerarRelatorio(converteData.parse(dataInicial), converteData.parse(dataFinal), tipoRelatorio);
                } catch (Exception ex) {
                    System.out.println("Erro ao gerar relatório!\n" + ex.getMessage());
                }
            }
        }
    }//GEN-LAST:event_bt_gerarRelatorioActionPerformed

